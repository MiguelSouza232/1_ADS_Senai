package com.mycompany.exerciciorepeticao;
public class Repeticao_6a190 {
    public static void main(String[] args) {
        int i, soma = 0;
        for (i = 6; i <= 190; i++) {
            soma = soma + i;
        }
        
        System.out.println("O resultado da soma dos números inteiros de 6 a 190 é: " + soma);
    }
    
}
